    private void btAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAceptarActionPerformed
        if(((String)ComboBox.getSelectedItem()).equals("Administrativos") == true){
            ver_empleados ventana1 = new ver_empleados();
            this.dispose();
            ventana1.setVisible(true);
        }
        if(((String)ComboBox.getSelectedItem()).equals("Vendedores") == true){
            ver2_empleados ventana2 = new ver2_empleados();
            this.dispose();
            ventana2.setVisible(true);
        }
        if(((String)ComboBox.getSelectedItem()).equals("Mecánicos") == true){
            ver3_empleados ventana3 = new ver3_empleados();
            this.dispose();
            ventana3.setVisible(true);
        }
        if(((String)ComboBox.getSelectedItem()).equals("Limpieza") == true){
            ver4_empleados ventana4 = new ver4_empleados();
            this.dispose();
            ventana4.setVisible(true);
        }
        if(((String)ComboBox.getSelectedItem()).equals("Vigilancia") == true){
            ver5_empleados ventana5 = new ver5_empleados();
            this.dispose();
            ventana5.setVisible(true);
        }
    }//GEN-LAST:event_btAceptarActionPerformed

